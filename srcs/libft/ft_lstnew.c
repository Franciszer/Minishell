#include "libft.h"

t_list		*ft_lstnew(void *content)
{
	t_list	*new;

	if (!content)
		return (NULL);
	if (!(new = (t_list *)malloc(sizeof(t_list))))
		return (NULL);
	new->content = content;
	new->next = NULL;
	return (new);
}
